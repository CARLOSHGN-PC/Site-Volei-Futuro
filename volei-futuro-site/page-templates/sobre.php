<section id="about-project">
    <h2>Sobre o Projeto Volei Futuro</h2>
    <p>
        O Volei Futuro é mais do que um time; é um projeto social que utiliza o esporte como ferramenta
        de transformação na vida de crianças e adolescentes. Nosso objetivo é oferecer um ambiente
        seguro e estimulante onde os jovens possam desenvolver suas habilidades atléticas,
        aprender sobre trabalho em equipe, disciplina e respeito.
    </p>

    <h3>Nossa Missão</h3>
    <p>
        Promover o desenvolvimento integral de nossos atletas, focando não apenas no desempenho
        esportivo, mas também na formação de cidadãos responsáveis e comprometidos com a comunidade.
    </p>

    <h3>Nossa Visão</h3>
    <p>
        Ser um projeto de referência no esporte de base, reconhecido pela excelência na formação
        de atletas e pelo impacto social positivo na região.
    </p>

    <h3>Nossos Valores</h3>
    <ul>
        <li><strong>Disciplina:</strong> Comprometimento com os treinos e com as regras.</li>
        <li><strong>Trabalho em Equipe:</strong> A união faz a força, dentro e fora da quadra.</li>
        <li><strong>Respeito:</strong> Pelos colegas, adversários, treinadores e pela comunidade.</li>
        <li><strong>Superação:</strong> Buscar sempre o melhor de si, superando desafios.</li>
    </ul>
</section>
